package Ex_01_Java_Basics;

public class Lab007_CommonMaths {
    public static void main(String[] args) {
        System.out.println(18+18);
        System.out.println(18-18);
        System.out.println(18*1);
        System.out.println(18/18);
        System.out.println(3/2);  // 3/2 will give 1 as println will give only integer part, no decimal
    }
}
