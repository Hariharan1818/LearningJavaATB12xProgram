package Ex_01_Java_Basics_Part_1;

public class Println_vs_Print {
    public static void main(String[] args) {
        System.out.println("Hi");
        System.out.println("Iam Hariharan"); // ln will produce a new line
        System.out.print("Hi");
        System.out.print("Iam Hariharan"); // print without ln will print in one line
    }
}
