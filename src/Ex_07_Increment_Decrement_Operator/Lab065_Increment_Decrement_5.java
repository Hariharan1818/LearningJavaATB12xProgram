package Ex_07_Increment_Decrement_Operator;

public class Lab065_Increment_Decrement_5 {
    public static void main(String[] args) {

        int a =10;

        System.out.println(++a + ++a);
        // first ++a value is 11
        // second ++a value is 12
        System.out.println(a);

        // so, value of a is 12
    }
}
