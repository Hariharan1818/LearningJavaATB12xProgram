package Ex_07_Increment_Decrement_Operator;

public class Lab061_Increment_Decrement_1 {
    public static void main(String[] args) {

        // Pre Increment

        int a =11; //"++a" denotes increment value by one [++a= a+1]
        int b= ++a; // before assigning the value of "a" it will be incremented by 1


        System.out.println(a);
        System.out.println(b);

    }
}
