package Ex_04_Operators;

public class Lab035_Logical_Operators {
    public static void main(String[] args) {

        //"&&" And operator
        // "||" Or operator
        // "!" Not operator

        boolean a = true;
        boolean b = false;

        System.out.println(a&&b);
        System.out.println(a||b);
        System.out.println(!b);
        System.out.println(!a);
        System.out.println(!!a);



    }
}
