package Ex_04_Operators;

public class Lab031_Arithmetic_Operators {
    public static void main(String[] args) {

        // Arithmetic operators
        // we can do addition, substraction, multiplication, division, and modulus[Remainder]
        // to perform arithmetic operator we need 2 operands

        int num1= 25;
        int num2=5;
        Float num3= 5.5f;

        System.out.println(num1*num2);
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1/num2);
        System.out.println(num1%num3);
        System.out.println(num1/num3);




    }
}
